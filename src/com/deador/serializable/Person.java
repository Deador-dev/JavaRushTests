package com.deador.serializable;

import java.io.Serializable;

public class Person implements Serializable {
    private String name;
    private int id;

    public Person() {

    }

    public Person(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", id=" + id +
                '}';
    }
}
